package com.totalcross.sample.pushnotification_firebase.util;

public class Args {

    private String args;

    public String getArgs() {
        return args;
    }

    public void setArgs(String args) {
        this.args = args;
    }

    @Override
    public String toString() {
        return "Args{" +
                "args='" + args + '\'' +
                '}';
    }
}